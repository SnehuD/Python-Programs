n1 = 10
n2 = 20

print('\n-----------------------BITWISE OPERATIONS-------------------------\n')
print('Number1\t\t : ', n1)
print('Number2\t\t : ', n2)

print('\nn1 & n2\t\t : ', n1 & n2)
print('n1 | n2\t\t : ', n1 | n2)
print('n1 ^ n2\t\t : ', n1 ^ n2)
print('n1 >> n2\t : ', n1 >> n2)
print('n1 << n2\t : ', n1 << n2)
print('~ n1\t\t : ', ~n1)
