str="Hello World!!"
#prints complete string
print(str)

#prints 1st character of the string
print(str[0])

#prints characters starting from 4th
print(str[3:6])

#prints characters starting from 5th
print(str[4:])

#prints original string two times
print(str*2)

#Concatination of two strings
print(str + "**")
